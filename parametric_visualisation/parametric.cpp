#include <iostream>
#include <vector>


int main() {

	using namespace std;

	const unsigned int X = 75;
	const unsigned int Y = 40;

	// Create an empty raster
	vector<bool> raster(X, false);

	// Initialise bitmap
	vector<vector<bool>> bitmap(Y, raster);

	// Some params
	struct P {

		unsigned int x;
		unsigned int y;
		unsigned int z;
	};

	vector<P> params = {
		{0xbf, 0x42, 0x6f},
		{0x31, 0xa1, 0x43},
		{0x8d, 0x61, 0xc6},
		{0xd6, 0xf8, 0x33},
		{0x39, 0xcb, 0x4e},
		{0x95, 0x24, 0xa4},
		{0xdd, 0xef, 0x2f},
		{0x38, 0xcd, 0x3b},
		{0xdc, 0x05, 0xf8},
		{0x9e, 0xda, 0xf1},
		{0x59, 0x1c, 0x34},
		{0x3d, 0x01, 0xc5},
		{0x54, 0xdb, 0xa4},
		{0x9f, 0x72, 0x8b},
		{0x1b, 0xc7, 0x7e},
		{0xc9, 0x89, 0x5b},
		{0x8f, 0xb9, 0xf3},
		{0xde, 0xc4, 0x37},
		{0x6c, 0xba, 0xcd},
		{0xc0, 0xe4, 0xaa},
	};

	for (const auto &p : params) {

		const unsigned int x = X * p.x / 0xff;
		const unsigned int y = Y * p.y / 0xff;

		bitmap[y][x] = true;
	}

	// Print bitmap
	for (const auto &y : bitmap) {

		for (const auto x : y)
			cout << (0 == x ? ' ' : '.');

		cout << '|' << endl;
	}

	// Add a nice bottom border
	cout << string(X, '_') << '|'<< endl;

	return 0;
}
